"""
实例003：完全平方数
题目：一个整数，它加上100后是一个完全平方数，再加上168又是一个完全平方数，请问该数是多少？
"""

"""
x + 100 = a^2
x + 100 + 168 = b^2
b^2 - a^2 = 168
(b-a)(b+a) = 168
(b-a)和(b+a)同号同为偶数，且是168的因数(包括168)
得到 a b 之后，可以得到 x
"""

# 收集所有满足条件的数
results = set()  # 集合去重

# 遍历168的所有整数因数d1（b-a）
for d1 in range(-168, 169):
    if d1 == 0:
        continue  # 除数不能为0
    if 168 % d1 == 0:
        d2 = 168 // d1  # d2 = b+a
        # 确保b和a都是整数（d1+d2必须是偶数）
        if (d1 + d2) % 2 == 0:
            # 计算b和a（整数）
            b = (d1 + d2) // 2
            a = (d2 - d1) // 2
            # 计算目标数x
            x = a * a - 100
            # 验证结果
            if (x + 100) == a * a and (x + 268) == b * b:
                results.add(x)

# 输出结果
for num in sorted(results):
    print(num)
